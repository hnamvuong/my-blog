<template>
    <div>
        <section class="content">
            <div class="container-fluid">
                <div class="row justify-content-around">
                    <!-- left column -->
                    <div class="col-md-6">
                        <!-- general form elements -->
                        <div class="card card-primary">
                            <div class="card-header">
                                <h3 class="card-title">Add New Category</h3>
                            </div>
                            <!-- /.card-header -->
                            <!-- form start -->
                            <form role="form" @click.prevent="addCategory()">
                                <div class="card-body">
                                    <div class="form-group">
                                        <label for="categoryId">Category Name</label>
                                        <input v-model="form.name" name="name" type="text" class="form-control"
                                               id="categoryId" placeholder="Add New Category"
                                               :class="{ 'is-invalid': form.errors.has('name') }">
                                        <has-error :form="form" field="name"></has-error>
                                    </div>
                                </div>
                                <!-- /.card-body -->

                                <div class="card-footer">
                                    <router-link class="btn btn-danger" to="/category-list">Back</router-link>
                                    <button type="submit" class="btn btn-primary">Save</button>
                                </div>
                            </form>
                        </div>
                        <!-- /.card -->
                    </div>
                </div>
                <!-- /.row -->
            </div><!-- /.container-fluid -->
        </section>
    </div>
</template>

<script>
    export default {
        name: "New",
        data() {
            return {
                // Create a new form instance
                form: new Form({
                    name : '',
                })
            }
        },
        methods: {
            addCategory() {
                this.form.post('/add-category').then((response) => {
                    this.$router.push('/category-list');
                    toast({
                        type: 'success',
                        title: 'Category Added Successfully'
                    })
                })
                .catch(() => {

                })
            }
        }
    }
</script>

